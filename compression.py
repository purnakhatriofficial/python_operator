number_one = 20
number_two = 10

equal_number = number_one == number_two

not_equal_number = number_one != number_two

greater_than_number = number_one > number_two

less_than_number = number_one < number_two

greater_or_equal_number = number_one >= number_two

less_or_equal = number_one <= number_two


print(f" Equal : {number_one} == {number_two} is {equal_number}")

print(f"Not equal : {number_one} != {number_two} is {not_equal_number}")

print(f"Greater Number  : {number_one} > {number_two} is {greater_than_number}")

print(f"Less than number : {number_one} < {number_two} is {less_than_number}")

print(f"Greater or equal number  : {number_one} >= {number_two} is {greater_or_equal_number} ")

print(f"Less or Equal number : {number_one} <= {number_two} is {less_or_equal}")

print("\n")

